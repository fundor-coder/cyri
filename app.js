function renderArticleBody(body) {
  return body
    .split(/\n{2,}/)
    .map((block) => {
      const heading = block.match(/^##\s+(.+)$/);
      return heading
        ? `<h3>${escapeHtml(heading[1])}</h3>`
        : `<p>${escapeHtml(block)}</p>`;
    })
    .join("");
}

